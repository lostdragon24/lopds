#!/bin/bash

# Имя выходного файла
OUTPUT="collected_sources.txt"

# Очищаем выходной файл, если он существует
> "$OUTPUT"

# Проходим по всем .c и .h файлам в текущей директории (без рекурсии)
for file in *.c *.h; do
    # Проверяем, что файл существует (на случай, если нет файлов с таким расширением)
    if [[ -f "$file" ]]; then
        echo "$file" >> "$OUTPUT"
        echo "" >> "$OUTPUT"
        cat "$file" >> "$OUTPUT"
        echo "" >> "$OUTPUT"  # Дополнительная пустая строка между файлами (по желанию)
    fi
done

echo "Сборка завершена. Результат сохранён в $OUTPUT"